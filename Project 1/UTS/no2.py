alas_segitiga = int(input("Masukkan alas segitiga : "))
tinggi_segitiga = int(input("Masukkan tinggi segitiga : "))
tinggi_limas = int(input("Masukkan tinggi limas : "))
Volume_Limas_Segitiga = (1/2*alas_segitiga*tinggi_segitiga)*tinggi_limas*1/3
print(f"Volume Limas Segitiga yang dihasilkan adalah {Volume_Limas_Segitiga} ")
